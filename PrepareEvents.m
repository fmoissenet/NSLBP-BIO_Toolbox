function Event = PrepareEvents(Event,n0,f)

if isfield(Event,'RHS')
    Event.RHS = fix(Event.RHS.*f)-n0+1;
    Event.LHS = fix(Event.LHS.*f)-n0+1;
    Event.RTO = fix(Event.RTO.*f)-n0+1;
    Event.LTO = fix(Event.LTO.*f)-n0+1;
end